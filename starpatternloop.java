

public class starpatternloop {
    public static void main(String[] args) {
        System.out.println("Printing Star Pattern.\n");
        
        int n = 5;
        for(int i=1;i<n;i++){
            for(int j=1;j<=n;j--){
            System.out.print("*");
        }
        System.out.print("\n");
    }     
            
    
        }
        
    }


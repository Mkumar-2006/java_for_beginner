public class Escapesequences {
    public static void main(String[] args) {
        System.out.println("Hello \nMohit"); // \n for a new line
        System.out.println("Hello \"Mohit\"...."); // \'....'\ we can put single and double codes
        System.out.println("*\n* *\n* * *\n* * * *");
        
    }
}
